#Programa que me dice si un número es: mayor, igual o menor a 5

numero = int(input("Escribe un número: "))
if numero > 5:
    print("El número es mayor que 5")
elif numero == 5:
    print("El número es igual a 5")
else:
    print("El número es menor a 5")
